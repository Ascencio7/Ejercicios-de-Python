""" 
    Escribir un programa que ingrese dos cadenas de texto y verificar si son 
    iguales o no.
"""
print("\nIgualdad de dos cadenas ingresadas")

cadeneOne = input("\nIngrese la primer cadena de texto: ")
cadeneTwo = input("Ingrese la segunda cadena de texto: ")

if cadeneOne == cadeneTwo:
    print("\nAmbas cadenas son iguales.")
    print("Fin del programa.\n")
else:
    print("\nLas dos cadenas no son iguales.")
    print("Fin del programa.\n")